def merge_dicts(*dicts):
    merged_dict = {}

    for d in dicts:
        for key, value in d.items():
            if key in merged_dict:
                # Append the new value to the existing list
                if value not in merged_dict[key]:  # To avoid duplicates
                    merged_dict[key].append(value)
            else:
                merged_dict[key] = [value]  # Initialize with a list

    return merged_dict


